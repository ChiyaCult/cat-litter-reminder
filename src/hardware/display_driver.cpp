const uint8_t *bitmapForMood(domain::Mood mood) {
  switch (mood) {
    case domain::Mood::Happy:     return kBitmapHappy;
    case domain::Mood::Content:   return kBitmapContent;
    case domain::Mood::Annoyed:   return kBitmapAnnoyed;
    case domain::Mood::Disgusted: return kBitmapDisgusted;
  }
  return kBitmapHappy;
}

